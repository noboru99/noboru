public class JKad02S{
	public static void main(String[] args){
		int x=2; //初期直は各自で決めてok
		int y=7; //初期直は各自で決めてok
		System.out.println("xの直は");
		System.out.println(x);
		System.out.println("yの直は");
		System.out.println(y);
		System.out.println("***xとyの直をいれかえます！***");
		System.out.println("xの直は");
		int z;
		z=x;
		x=y;
		y=z;
		System.out.println(x);
		System.out.println("yの直は");
		System.out.println(y);
	}
}
		