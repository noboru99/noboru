public class JKad03X0 {
	// xの値を表示
	public static void printValue(int x) {
		System.out.println("受け取った引数の値は" + x + "です！");
	}
	// mainメソッド
	public static void main(String[] args) {
		printValue(1);			// 引数xに1を渡す
		printValue(2);			// 引数xに2を渡す
		printValue(3);			// 引数xに3を渡す
	}
}
