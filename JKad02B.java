public class JKad02B{
	public static void main(String[] args){
		System.out.println("変数xの直を表示します!");
		int x=5;
		System.out.println(x);
		
		System.out.println("変数xに１４を足します!");
		x=x+14;
		System.out.println(x);
		System.out.println("変数xから３をひきます！");
		x=x-3;
		System.out.println(x);
		System.out.println("変数xから7をかけます！");
		x=x*7;
		System.out.println(x);
		System.out.println("変数xを１０で割ります！");
		x=x/10;
		System.out.println(x);
		System.out.println("変数xを4で割った余りを求めます！");
		x=x%4;
		System.out.println(x);
	}
	
}